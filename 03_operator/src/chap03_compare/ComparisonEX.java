package chap03_compare;

public class ComparisonEX {

  public static void main(String[] args) {
    
    // 비교 연산 
    
    int x = 10;
    int y = 20;
    
    System.out.println(x > y);
    System.out.println(x >= y);
    System.out.println(x < y);
    System.out.println(x <= y);
    System.out.println(x == y);
    System.out.println(x != y);
    
  }

}
